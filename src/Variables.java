public class Variables {
    public static void main(String[] args) {

        byte edad=35; // primero el tipo de variable, luego el nombre de la variable

        System.out.print(edad); 
        /* La diferencia entre print y println es que println imprime en una nueva línea,
        mientras que print hace que la próxima impresión continúe a la presente */

        edad=75;
        
        System.out.println(edad); // el println introduce un "enter" al finalizar la impresión
    }
}